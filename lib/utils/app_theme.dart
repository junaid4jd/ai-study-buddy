  static Color withOpacity(Color color, double opacity) {
    // Clamp opacity between 0.0 and 1.0, then convert to 0..255 alpha
    final alpha = (opacity.clamp(0.0, 1.0) * 255).round();
    return color.withAlpha(alpha);
  }

